export class RegisterAddress {
    houseNumber: number;
    street: string;
    city: string;
    state: string;
    zipCode: number;

 
    constructor(houseNumber: number, street: string, city: string, state: string, zipCode: number ) {
        this.houseNumber = houseNumber;
        this.street = street;
        this.city = city;
        this.state = state;
        this.zipCode = zipCode;
       
        
    }




}